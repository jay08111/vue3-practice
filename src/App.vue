<template>
  <h1>{{ title }}</h1>
  <div v-if="showModal">
    <Modal
      :header="header"
      :text="text"
      theme="sale"
      :showModal="showModal"
      @close="toggleModal"
    />
  </div>
  <button @click="toggleModal">open modal</button>
</template>

<script>
import Modal from "./components/Modal.vue";
export default {
  name: "App",
  components: {
    Modal,
  },
  data() {
    return {
      title: "my first vue app !",
      header: "sign up for giveaway",
      text: "hello world",
      showModal: false,
    };
  },
  methods: {
    toggleModal() {
      this.showModal = !this.showModal;
    },
  },
};
</script>

